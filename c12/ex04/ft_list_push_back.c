#include "ft_list.h"

void	ft_list_push_back(t_list **begin_list, void *data)
{
	t_list	*elem;
	t_list	*current;

	elem = ft_create_elem(data);
	if (elem == NULL)
		return (NULL);
	if (begin_list == NULL)
		*begin_list = elem;
	else
	{
		current = *begin_list;
		while (current->next != NULL)
		{
			current->next = current;
		}
		current->next = elem;
	}
}
